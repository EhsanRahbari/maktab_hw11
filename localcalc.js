const calc = {
    sum:function(arg1 , arg2){
        return arg1 + arg2;
    },
    minus:function(arg1 , arg2){
        return arg1 - arg2;
    },
    mul:function(arg1 , arg2){
        return arg1 * arg2;
    },
    div:function(arg1 , arg2){
        return arg1 / arg2;
    }
}
module.exports = calc;